// Function to get a random interviewer profile
export function getRandomInterviewer() {
  const randomIndex = Math.floor(Math.random() * interviewerProfiles.length);
  return interviewerProfiles[randomIndex];
}

// Function to get an interviewer by gender preference (optional)
export function getInterviewerByGender(preferredGender) {
  const matchingProfiles = interviewerProfiles.filter(profile => 
    profile.gender.toLowerCase() === preferredGender.toLowerCase()
  );
  
  if (matchingProfiles.length === 0) {
    return getRandomInterviewer(); // Fallback to random if no matches
  }
  
  const randomIndex = Math.floor(Math.random() * matchingProfiles.length);
  return matchingProfiles[randomIndex];
}

// Function to get an interviewer by industry/background (optional)
export function getInterviewerByIndustry(industry) {
  // Simple keyword matching - could be improved with more sophisticated matching
  const matchingProfiles = interviewerProfiles.filter(profile => 
    profile.company.toLowerCase().includes(industry.toLowerCase()) ||
    profile.background.toLowerCase().includes(industry.toLowerCase()) ||
    profile.title.toLowerCase().includes(industry.toLowerCase())
  );
  
  if (matchingProfiles.length === 0) {
    return getRandomInterviewer(); // Fallback to random if no matches
  }
  
  const randomIndex = Math.floor(Math.random() * matchingProfiles.length);
  return matchingProfiles[randomIndex];
}
